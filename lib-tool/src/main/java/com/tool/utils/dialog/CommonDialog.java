import android.app.Dialog;
import android.content.Context;
import android.os.Bundle;
import android.text.TextUtils;
import android.util.DisplayMetrics;
import android.view.LayoutInflater;
import android.view.View;
import android.view.Window;
import android.view.WindowManager;
import android.widget.TextView;

import com.tool.R;


/**********************************************************
 *                                                        *
 *                  Created by wucongpeng on 2017/4/18.        *
 **********************************************************/


public class CommonDialog extends Dialog {

    private Context context;
    private String title;
    private String error_content;
    private ClickListenerInterface clickListenerInterface;

    public CommonDialog(Context context, String error_content) {
        super(context, R.style.MyDialogStyle);
        this.context = context;
        this.error_content = error_content;
    }

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        init();
    }

    private void init() {
        LayoutInflater inflater = LayoutInflater.from(context);
        View view = inflater.inflate(R.layout.dialog_common, null);
        setContentView(view);
        TextView tv_ok = (TextView) view.findViewById(R.id.tv_ok);
        TextView tv_error_content = (TextView) view.findViewById(R.id.id_error_content);
        tv_ok.setOnClickListener(new clickListener());

        if (!TextUtils.isEmpty(error_content)){
            tv_error_content.setText(error_content);
        }


        Window dialogWindow = getWindow();
        WindowManager.LayoutParams lp = dialogWindow.getAttributes();
        DisplayMetrics d = context.getResources().getDisplayMetrics(); // 获取屏幕宽、高用
        lp.width = (int) (d.widthPixels * 0.8); // 高度设置为屏幕的0.6
        dialogWindow.setAttributes(lp);
    }

    public interface ClickListenerInterface {
        void doConfirm();
    }

    public void setClicklistener(ClickListenerInterface clickListenerInterface) {
        this.clickListenerInterface = clickListenerInterface;
    }

    private class clickListener implements View.OnClickListener {
        @Override
        public void onClick(View v) {
            int id = v.getId();
            if (id == R.id.tv_ok) {
                dismiss();

            }
        }
    }
}
